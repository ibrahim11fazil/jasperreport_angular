package qa.gov.customs.employee.models;

public class Department {

   String  orgunit;
   String  orgunitDescAr;
   String  orgunitDesc;

    public String getOrgunit() {
        return orgunit;
    }

    public void setOrgunit(String orgunit) {
        this.orgunit = orgunit;
    }

    public String getOrgunitDescAr() {
        return orgunitDescAr;
    }

    public void setOrgunitDescAr(String orgunitDescAr) {
        this.orgunitDescAr = orgunitDescAr;
    }

    public String getOrgunitDesc() {
        return orgunitDesc;
    }

    public void setOrgunitDesc(String orgunitDesc) {
        this.orgunitDesc = orgunitDesc;
    }
}
